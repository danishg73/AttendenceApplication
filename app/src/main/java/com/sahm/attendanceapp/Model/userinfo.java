package com.sahm.attendanceapp.Model;

public class userinfo
{
    String name, key,phone,email,password, type, attendance,username;



    public userinfo() {
    }

    public userinfo(String key,String username, String phone, String email, String password, String type, String attendance,String name) {
        this.key = key;
        this.phone = phone;
        this.email = email;
        this.password = password;
        this.type = type;
        this.attendance = attendance;
        this.name=name;
        this.username=username;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getAttendance() {
        return attendance;
    }

    public void setAttendance(String attendance) {
        this.attendance = attendance;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
